// Helper function to get leads with follow-up today
export const getTodayFollowups = (leads: Lead[], today: string): Lead[] => {
   return leads.filter(lead => lead.next_followup === today && lead.status !== 'closed');
};

// Helper function to get leads by status
export const getLeadsByStatus = (leads: Lead[], status: LeadStatus): Lead[] => {
   return leads.filter(lead => lead.status === status);
};

